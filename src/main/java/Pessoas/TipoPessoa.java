package Pessoas;
/**
 * This enum represents the types of people in a system.
 * The types of people include doctor, nurse, auxiliary, administrative, patient, and visitor.
 */
public enum TipoPessoa {
    MEDICO, // Represents a doctor
    ENFERMEIRO, // Represents a nurse
    AUXILIAR, // Represents an auxiliary
    ADMINISTRATIVO, // Represents an administrative
    UTENTE, // Represents a patient
    VISITANTE // Represents a visitor
}